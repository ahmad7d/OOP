package ascii_art;

/**
 * The User interface defines the behavior for classes that interact with users.
 * Classes implementing this interface should provide their own implementation
 * for the run() method to execute user interaction logic.
 */
public interface User {

    /**
     * Executes the main functionality related to user interaction.
     * Classes implementing this method should define the logic for user input
     * processing and interaction.
     */
    void run();
}
